static void write_pixel8x(struct display *display, unsigned x, unsigned y, unsigned c) {
	unsigned wd_in_bytes;
	unsigned off;

	wd_in_bytes = display->width / 4;
	off = wd_in_bytes * y + x / 4;
	set_plane(x & 3);
	vpokeb(off, c);
}
