void write_pixel1(struct display *display, unsigned x, unsigned y, unsigned c)
{
	unsigned wd_in_bytes;
	unsigned off, mask;

	c = (c & 1) * 0xFF;
	wd_in_bytes = display->width / 8;
	off = wd_in_bytes * y + x / 8;
	x = (x & 7) * 1;
	mask = 0x80 >> x;
	vpokeb(off, (vpeekb(off) & ~mask) | (c & mask));
}

void write_pixel2(struct display *display, unsigned x, unsigned y, unsigned c) {
	unsigned wd_in_bytes, off, mask;

	c = (c & 3) * 0x55;
	wd_in_bytes = display->width / 4;
	off = wd_in_bytes * y + x / 4;
	x = (x & 3) * 2;
	//mask = 0xC0 >> x;
	mask = 0x03 << (sizeof(mask) - x);
	vpokeb(off, (vpeekb(off) & ~mask) | (c & mask));
}





void write_pixel8x(struct display *display, unsigned x, unsigned y, unsigned c) {
	unsigned wd_in_bytes;
	unsigned off;

	wd_in_bytes = display->width / 4;
	off = wd_in_bytes * y + x / 4;
	set_plane(x & 3);
	vpokeb(off, c);
}

